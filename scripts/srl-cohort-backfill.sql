-- 3. Normalisation helper — matches normaliseComponent() in
--    api/profile-save.js bit-for-bit:
--       lowercase
--       '&' → ' and '
--       any run of non-alphanumeric → '-'
--       trim leading/trailing '-'
CREATE OR REPLACE FUNCTION pg_temp.bp_normalise_component(s text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT trim(BOTH '-' FROM regexp_replace(
    lower(replace(COALESCE(s, ''), '&', ' and ')),
    '[^a-z0-9]+',
    '-',
    'g'
  ));
$$;

-- 4. Cohort-id helper — matches computeCohortId() in
--    api/profile-save.js. Returns NULL when industries is empty
--    or state is missing.
CREATE OR REPLACE FUNCTION pg_temp.bp_compute_cohort_id(
  industries text[],
  state_in   text,
  postcode_in text
)
RETURNS text
LANGUAGE plpgsql
IMMUTABLE
AS $$
DECLARE
  norm_industries text[];
  state_part      text;
  region_part     text;
  postcode_padded text;
  sa4_name        text;
BEGIN
  -- Normalise + dedupe + sort industries
  SELECT array_agg(DISTINCT n ORDER BY n)
  INTO   norm_industries
  FROM   (
    SELECT pg_temp.bp_normalise_component(i) AS n
    FROM   unnest(COALESCE(industries, ARRAY[]::text[])) AS i
  ) sub
  WHERE  n IS NOT NULL AND n != '';

  IF norm_industries IS NULL
     OR array_length(norm_industries, 1) IS NULL
     OR array_length(norm_industries, 1) = 0 THEN
    RETURN NULL;
  END IF;

  state_part := pg_temp.bp_normalise_component(state_in);
  IF state_part IS NULL OR state_part = '' THEN
    RETURN NULL;
  END IF;

  -- Postcode lookup. NULL/empty postcode, non-4-digit input, or
  -- a postcode missing from the lookup table all resolve to
  -- 'no-region' (same as endpoint behaviour).
  IF postcode_in IS NULL OR btrim(postcode_in) = '' THEN
    region_part := 'no-region';
  ELSE
    postcode_padded := lpad(btrim(postcode_in), 4, '0');
    IF postcode_padded !~ '^[0-9]{4}$' THEN
      region_part := 'no-region';
    ELSE
      SELECT sa4 INTO sa4_name
      FROM   _backfill_postcode_sa4
      WHERE  postcode = postcode_padded;
      IF sa4_name IS NULL THEN
        region_part := 'no-region';
      ELSE
        region_part := pg_temp.bp_normalise_component(sa4_name);
        IF region_part IS NULL OR region_part = '' THEN
          region_part := 'no-region';
        END IF;
      END IF;
    END IF;
  END IF;

  RETURN array_to_string(norm_industries, '|') || '::' || state_part || '::' || region_part;
END;
$$;

-- 5. Backfill UPDATE. Skips rows that lack the cohort-determining
--    fields (industry NULL/empty, state NULL/empty). Idempotent via
--    IS DISTINCT FROM — a second run computes the same value and
--    matches zero rows.
UPDATE profiles p
SET    cohort_id = pg_temp.bp_compute_cohort_id(
         p.industry,
         p.address_state,
         p.address_postcode
       )
WHERE  p.industry IS NOT NULL
   AND array_length(p.industry, 1) IS NOT NULL
   AND array_length(p.industry, 1) > 0
   AND p.address_state IS NOT NULL
   AND btrim(p.address_state) != ''
   AND p.cohort_id IS DISTINCT FROM pg_temp.bp_compute_cohort_id(
         p.industry,
         p.address_state,
         p.address_postcode
       );

-- 6. Inspection view (commit anyway — the temp table drops on commit)
SELECT id,
       industry,
       address_state,
       address_postcode,
       cohort_id
FROM   profiles
ORDER  BY created_at NULLS LAST;

COMMIT;

